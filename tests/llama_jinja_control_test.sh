#!/usr/bin/env bash
# Smoke test for station/llama-jinja/llama-control.sh: the three-alias working set
# (qwen36-35b, qwen3-coder-30b, glm-z1-32b) and the `use`/`models`/`status` path.
# Hermetic: the stack is copied into the sandbox so `use` writes .env there, never
# touching the repo. No live server needed — compose and curl are faked via the
# script's LLAMA_COMPOSE / CURL_BIN seams.
set -uo pipefail
source "$(dirname "$0")/lib.sh"
ps_setup_sandbox

STACK="$PS_SANDBOX/llama-jinja"
cp -R "$PS_REPO_ROOT/station/llama-jinja" "$STACK"
CTL="$STACK/llama-control.sh"
WORKING_SET="qwen36-35b qwen3-coder-30b glm-z1-32b qwen35-4b qwen3-0.6b"

# --- static: the three working-set env files are well-formed -----------------
for a in $WORKING_SET; do
  f="$STACK/models/$a.env"
  assert_file "$f" "env file $a exists"
  body="$(cat "$f")"
  assert_contains "$body" "# alias: $a"      "$a has alias header (active_alias depends on it)"
  assert_contains "$body" "--alias $a"       "$a LLAMA_ARGS sets --alias"
  assert_contains "$body" "--model /models/" "$a LLAMA_ARGS sets --model path"
  # env-file parser has no line continuation: LLAMA_ARGS must be ONE physical line.
  assert_eq "$(grep -c '^LLAMA_ARGS=' "$f")" "1" "$a has exactly one LLAMA_ARGS line"
  assert_eq "$(wc -l < "$f" | tr -d ' ')"    "3" "$a env is 3 lines (alias/image/args)"
done

# --- fake compose seam (records its argv) ------------------------------------
CALLLOG="$PS_SANDBOX/compose.log"; : > "$CALLLOG"
fcompose="$PS_SANDBOX/fake-compose"
cat > "$fcompose" <<EOF
#!/usr/bin/env bash
printf '%s\n' "\$*" >> "$CALLLOG"
exit 0
EOF
chmod +x "$fcompose"
export LLAMA_COMPOSE="$fcompose"

# --- models lists the full registry -----------------------------------------
m="$("$CTL" models)"
for a in $WORKING_SET qwopus-27b qwen-9b; do
  assert_contains "$m" "$a" "models lists $a"
done

# --- use across the three: active alias tracks, .env written, compose called -
for a in $WORKING_SET; do
  "$CTL" use "$a" >/dev/null 2>&1
  active="$("$CTL" models | sed -n 's/^  \* //p' | head -1)"
  assert_eq "$active" "$a" "use $a -> active alias = $a"
done
assert_file "$STACK/.env" ".env generated by use"
assert_contains "$(cat "$CALLLOG")" "up -d --force-recreate" "use invokes compose force-recreate"

# --- a bad alias is rejected (no .env clobber) -------------------------------
"$CTL" use no-such-model >/dev/null 2>&1; rc=$?
assert_eq "$rc" "1" "use <bad alias> exits nonzero"

# --- status readiness logic via fake curl -----------------------------------
# 200/200 -> READY
fc_ok="$PS_SANDBOX/fake-curl-ok"
cat > "$fc_ok" <<'SH'
#!/usr/bin/env bash
for x in "$@"; do
  case "$x" in
    *"/v1/chat/completions"*) printf '200'; exit 0 ;;
    *"/v1/messages"*)         printf '200'; exit 0 ;;
  esac
done
printf '000'; exit 0
SH
chmod +x "$fc_ok"
out_ok="$(CURL_BIN="$fc_ok" "$CTL" status)"
if printf '%s\n' "$out_ok" | grep -qx 'READY'; then r=yes; else r=no; fi
assert_eq "$r" "yes" "status READY on 200/200"
assert_contains "$out_ok" "Anthropic /v1/messages=200" "status reports the Anthropic probe code"

# non-200 -> NOT READY
fc_busy="$PS_SANDBOX/fake-curl-busy"
cat > "$fc_busy" <<'SH'
#!/usr/bin/env bash
printf '503'; exit 0
SH
chmod +x "$fc_busy"
out_busy="$(CURL_BIN="$fc_busy" "$CTL" status)"
if printf '%s\n' "$out_busy" | grep -q '^NOT READY'; then r=yes; else r=no; fi
assert_eq "$r" "yes" "status NOT READY when a probe != 200"

# --- bonus: real `docker compose config` validates .env double-duty ----------
# (.env is both compose's interpolation source for ${LLAMA_IMAGE} and the
#  env_file injected for the runtime $LLAMA_ARGS expansion.)
if command -v docker >/dev/null 2>&1; then
  for a in $WORKING_SET; do
    cp "$STACK/models/$a.env" "$STACK/.env"
    cfg="$(cd "$STACK" && docker compose config 2>/dev/null)"; crc=$?
    assert_eq "$crc" "0" "docker compose config parses for $a"
    assert_contains "$cfg" "sha256:c4d2aaf10abd" "compose resolves \${LLAMA_IMAGE} digest for $a"
    assert_contains "$cfg" "LLAMA_ARGS" "compose command keeps \$LLAMA_ARGS for runtime split ($a)"
  done
else
  echo "  (skip: docker not present — compose config validation skipped)"
fi

ps_teardown_sandbox
ps_report; exit $?
